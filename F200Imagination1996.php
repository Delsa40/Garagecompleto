                Fue un estudio conceptual de Daimler-Benz presentado en el Salón del Automóvil de París de 1996. El 
                objetivo era mostrar las innovaciones en control, diseño y comodidad en los turismos. El diseño 
                exterior del automóvil se basó en parte en la próxima Clase S.<br><br>
                Una de las innovaciones únicas fue la implementación de "Sidesticks". Las palancas laterales eran un 
                diseño de palanca de mando (en lugar del volante convencional y los pedales de acelerador/freno) que 
                se podían alternar entre el conductor y el pasajero. Además, contaba con puertas estilo mariposa, vidrio 
                de techo panorámico electrotransparente, cámaras de video en lugar de espejos retrovisores, Window-Airbag, 
                sistema de control de manejo dinámico con visión de futuro, suspensión totalmente activa Active Body Control, 
                sistema de faros con distribución variable de la luz y reconocimiento de voz para teléfono móvil. La mayoría 
                de estas innovaciones serían utilizadas luego en modelos de producción de Mercedes Benz y Maybach.<br><br>
